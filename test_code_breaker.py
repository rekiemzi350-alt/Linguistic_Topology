def test_sequence_against_language(sequence, lang_name, len_func):
    print(f"Testing against {lang_name} Logic...")
    matches = 0
    total_checks = len(sequence) - 1
    
    for i in range(total_checks):
        current_val = sequence[i]
        expected_next = sequence[i+1]
        
        # Calculate what the next number SHOULD be in this language
        predicted_next = current_val + len_func(current_val)
        
        if predicted_next == expected_next:
            print(f"  [âœ“] Match: {current_val} -> {expected_next}")
            matches += 1
        else:
            print(f"  [X] Fail:  {current_val} -> {predicted_next} (Expected {expected_next})")
            
    score = (matches / total_checks) * 100
    print(f"  >> {lang_name} Confidence Score: {score:.1f}%\n")

